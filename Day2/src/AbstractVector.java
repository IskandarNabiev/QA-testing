public abstract class AbstractVector {
    public abstract Vector2d sumVec(Vector2d x);
    public abstract Vector2d subVec(Vector2d x);
    public abstract double scalar(Vector2d x);

    public abstract Vector3d sumVec3d(Vector3d x);
    public abstract Vector3d subVec3d(Vector3d x);
    public abstract double scalar3d(Vector3d x);
}
